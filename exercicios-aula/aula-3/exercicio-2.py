## Utilização e substituição nas variáveis (parte 4)

livroPreferido = 'O caçador de Pipas'
print('Meu livro preferido é', livroPreferido)

## Você pode também intercalar muitas variáveis e textos na mesma frase:

cidadeNatal = 'Atibaia'
cidadeAtual = 'Rio de Janeiro'

print('Nasci em',cidadeNatal,', mas hoje moro no', cidadeAtual)

## Podemos pedir que o usuário insira seus dados através do comando input:

escolaridade = input()
print('Sua escolaridade é:',escolaridade)

## Mais opções de interação

escolaridade = input('Qual a sua escolaridade? ')
print('Sua escolaridade é:',escolaridade)

### Exercício

nome = input('Qual seu nome? ')
